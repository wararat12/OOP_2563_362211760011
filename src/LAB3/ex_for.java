package LAB3;

public class ex_for {
    public static void main(String[] args) {

        // 1. พิมพ์ข้อความ Hello 10 ครั้ง
        for (int i = 1; i <= 10; i++) {
            System.out.println("Hello" +i);  // ถ้ามี 1 คำสั่งไม่จำเป็นต้องใส่เครื่องหมาย { } ก็ได้  ถ้ามีหลายคำสั่ง ต้องใส่ { }
            System.out.println("MIT");
        }
        System.out.println("Good Bye");




    } // main

} //class
